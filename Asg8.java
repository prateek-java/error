import java.util.*;
class Asg8
{
	public static void main(String args[])
	{
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Take input: ");
		int i = sc.nextInt();
		
		for(int j=1;j<=10;j++)
		{
			int k = i*j;
			System.out.println(i+ "X" +j+ "=" +k);
		}
	}
}
		
		